      SUBROUTINE CROP
!     APEX1905
!     THIS SUBPROGRAM PREDICTS DAILY POTENTIAL GROWTH OF TOTAL PLANT
!     BIOMASS AND ROOTS.
      USE PARM
      IF(ROSP(ISA)>0.)THEN
          XX=.685-.209*ROSP(ISA)
      ELSE
          XX=EXTC(JJK)
      END IF
      PAR=.0005*SRAD(IRF(ISA))*(1.-EXP(-XX*SLAI(JJK,ISA)))
      X1=MAX(VPD-1.,-.5)
      XX=WA(JJK)-WAVP(JJK)*X1
      X2=MAX(1.,HRLT/12.)
      DDM(JJK,ISA)=MAX(0.,XX*PAR)*X2
      DM(JJK,ISA)=DM(JJK,ISA)+DDM(JJK,ISA)
      RETURN
      END