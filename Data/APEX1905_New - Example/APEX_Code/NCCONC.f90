      SUBROUTINE NCCONC
      ! APEX1905
      !	THIS SUBROUTINE CALCULATES GAS CONCENTRATIONS IN LIQUID AND 
      !	AIR PHASES
      USE PARM
	  DO J=1,NBCL
	      ! CLO2=CONC GAS IN LIQ PHASE (G/M3 WATER)
          CLO2(J,ISA)=AO2C(J,ISA)/(AFP(J,ISA)*HKPO(J,ISA)+VWC(J,ISA))   
          ! CGO2=CONC GAS IN GAS PHASE (G/M3 AIR)
          CGO2(J,ISA)=AO2C(J,ISA)/(TPOR(J,ISA)+VWC(J,ISA)*(1./HKPO(J,ISA)-1.))
          ! CLCO2=CONC GAS IN LIQ PHASE
          CLCO2(J,ISA)=ACO2C(J,ISA)/(AFP(J,ISA)*HKPC(J,ISA)+VWC(J,ISA))   
          ! CGCO2=CONC GAS IN GAS PHASE
          CGCO2(J,ISA)=ACO2C(J,ISA)/(TPOR(J,ISA)+VWC(J,ISA)*(1./HKPC(J,ISA)-1.))
          ! CLN2O=CONC GAS IN LIQ PHASE
	      CLN2O(J,ISA)=AN2OC(J,ISA)/(AFP(J,ISA)*HKPN(J,ISA)+VWC(J,ISA)) 
          ! CGN2O=CONC GAS IN GAS PHASE
          CGN2O(J,ISA)=AN2OC(J,ISA)/(TPOR(J,ISA)+VWC(J,ISA)*(1./HKPN(J,ISA)-1.))
      END DO
      RETURN
      END