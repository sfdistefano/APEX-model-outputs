      SUBROUTINE NEVN
!     APEX1905
!     THIS SUBPROGRAM ESTIMATES UPWARD SOL N MOVEMENT CAUSED BY SOIL 
!     EVAPORATION.
      USE PARM
      IF(NEV==1)RETURN
      LD1=LID(1,ISA)
      TOT=0.
      DO KK=NEV,2,-1
          ISL=LID(KK,ISA)
          IF(WNO3(ISL,ISA)<.001)CYCLE
          XX=SEV(ISL,ISA)*PRMT(86)*WNO3(ISL,ISA)/(SWST(ISL,ISA)+SEV(ISL,ISA))
          TOT=TOT+XX
          WNO3(ISL,ISA)=MAX(1.E-5,WNO3(ISL,ISA)-XX)
      END DO
      WNO3(LD1,ISA)=WNO3(LD1,ISA)+TOT
      RETURN
      END