      SUBROUTINE SWN585
      ! APEX1905
      ! THIS SUBPROGRAM COMPUTES THE SOIL WATER CONTENT(m/m) AND NO3 
      ! CONCENTRATION(G/M^3) OF A SOIL AT 0.15 AND 0.3 M DEPTHS. 
      USE PARM
      SWH05=0.
      SWH15=0.
      SWH25=0.
      SWH35=0.
      SWH45=0.
      SWH55=0.
      SWH65=0.
      SWH75=0.
      SWH85=0.
!
      SNO05=0.
      SNO15=0.
      SNO25=0.
      SNO35=0.
      SNO45=0.
      SNO55=0.
      SNO65=0.
      SNO75=0.
      SNO85=0.
!      
      SNH05=0.
      SNH15=0.
      SNH25=0.
      SNH35=0.
      SNH45=0.
      SNH55=0.
      SNH65=0.
      SNH75=0.
      SNH85=0.
!    
      WT05=0.
      WT15=0.
      WT25=0.
      WT35=0.
      WT45=0.
      WT55=0.
      WT65=0.
      WT75=0.
      WT85=0.
!    
	  DO I=1,NBSL(ISA)
	      L=LID(I,ISA)
	      IF(Z(L,ISA)>.05)GO TO 2
	      SWH05=SWH05+SWST(L,ISA)
	      SNO05=SNO05+WNO3(L,ISA)
	      SNH05=SNH05+WNH3(L,ISA)
	      WT05=WT05+WT(L,ISA)
	      L1=L
	  END DO
      GO TO 11
    2 RTO=(.05-Z(L1,ISA))/(Z(L,ISA)-Z(L1,ISA))
      SWH05=SWH05+RTO*SWST(L,ISA)
      SNO05=SNO05+RTO*WNO3(L,ISA)
      SNH05=SNH05+RTO*WNH3(L,ISA)
	  WT05=WT05+RTO*WT(L,ISA)
	  DO J=1,NBSL(ISA)
	      L=LID(J,ISA)
	      IF(Z(L,ISA)>.15)GO TO 3
	      SWH15=SWH15+SWST(L,ISA)
	      SNO15=SNO15+WNO3(L,ISA)
	      SNH15=SNH15+WNH3(L,ISA)
	      WT15=WT15+WT(L,ISA)
	      L1=L
	  END DO
      GO TO 12
    3 RTO=(.15-Z(L1,ISA))/(Z(L,ISA)-Z(L1,ISA))
      SWH15=SWH15+RTO*SWST(L,ISA)
      SNO15=SNO15+RTO*WNO3(L,ISA)
      SNH15=SNH15+RTO*WNH3(L,ISA)
	  WT15=WT15+RTO*WT(L,ISA)
	  DO J=1,NBSL(ISA)
	      L=LID(J,ISA)
	      IF(Z(L,ISA)>.25)GO TO 4
	      SWH25=SWH25+SWST(L,ISA)
	      SNO25=SNO25+WNO3(L,ISA)
	      SNH25=SNH25+WNH3(L,ISA)
	      WT25=WT25+WT(L,ISA)
	      L1=L
	  END DO
      GO TO 13
    4 RTO=(.25-Z(L1,ISA))/(Z(L,ISA)-Z(L1,ISA))
      SWH25=SWH25+RTO*SWST(L,ISA)
      SNO25=SNO25+RTO*WNO3(L,ISA)
      SNH25=SNH25+RTO*WNH3(L,ISA)
	  WT25=WT25+RTO*WT(L,ISA)
	  DO I=1,NBSL(ISA)
	      L=LID(I,ISA)
	      IF(Z(L,ISA)>.35)GO TO 5
	      SWH35=SWH35+SWST(L,ISA)
	      SNO35=SNO35+WNO3(L,ISA)
	      SNH35=SNH35+WNH3(L,ISA)
	      WT35=WT35+WT(L,ISA)
	      L1=L
	  END DO
      GO TO 14
5     RTO=(.35-Z(L1,ISA))/(Z(L,ISA)-Z(L1,ISA))
      SWH35=SWH35+RTO*SWST(L,ISA)
      SNO35=SNO35+RTO*WNO3(L,ISA)
      SNH35=SNH35+RTO*WNH3(L,ISA)
	  WT35=WT35+RTO*WT(L,ISA)
	  DO J=1,NBSL(ISA)
	      L=LID(J,ISA)
	      IF(Z(L,ISA)>.45)GO TO 6
	      SWH45=SWH45+SWST(L,ISA)
	      SNO45=SNO45+WNO3(L,ISA)
	      SNH45=SNH45+WNH3(L,ISA)
	      WT45=WT45+WT(L,ISA)
	      L1=L
	  END DO
      GO TO 15
6     RTO=(.45-Z(L1,ISA))/(Z(L,ISA)-Z(L1,ISA))
      SWH45=SWH45+RTO*SWST(L,ISA)
      SNO45=SNO45+RTO*WNO3(L,ISA)
      SNH45=SNH45+RTO*WNH3(L,ISA)
	  WT45=WT45+RTO*WT(L,ISA)
	  DO J=1,NBSL(ISA)
	      L=LID(J,ISA)
	      IF(Z(L,ISA)>.55)GO TO 7
	      SWH55=SWH55+SWST(L,ISA)
	      SNO55=SNO55+WNO3(L,ISA)
	      SNH55=SNH55+WNH3(L,ISA)
	      WT55=WT55+WT(L,ISA)
	      L1=L
	  END DO
      GO TO 16
7     RTO=(.55-Z(L1,ISA))/(Z(L,ISA)-Z(L1,ISA))
      SWH55=SWH55+RTO*SWST(L,ISA)
      SNO55=SNO55+RTO*WNO3(L,ISA)
      SNH55=SNH55+RTO*WNH3(L,ISA)
	  WT55=WT55+RTO*WT(L,ISA)
	  DO J=1,NBSL(ISA)
	      L=LID(J,ISA)
	      IF(Z(L,ISA)>.65)GO TO 8
	      SWH65=SWH65+SWST(L,ISA)
	      SNO65=SNO65+WNO3(L,ISA)
	      SNH65=SNH65+WNH3(L,ISA)
	      WT65=WT65+WT(L,ISA)
	      L1=L
	  END DO
      GO TO 17
8     RTO=(.65-Z(L1,ISA))/(Z(L,ISA)-Z(L1,ISA))
      SWH65=SWH65+RTO*SWST(L,ISA)
      SNO65=SNO65+RTO*WNO3(L,ISA)
      SNH65=SNH65+RTO*WNH3(L,ISA)
	  WT65=WT65+RTO*WT(L,ISA)
	  DO J=1,NBSL(ISA)
	      L=LID(J,ISA)
	      IF(Z(L,ISA)>.75)GO TO 9
	      SWH75=SWH75+SWST(L,ISA)
	      SNO75=SNO75+WNO3(L,ISA)
	      SNH75=SNH75+WNH3(L,ISA)
	      WT75=WT75+WT(L,ISA)
	      L1=L
	  END DO
      GO TO 18
9     RTO=(.75-Z(L1,ISA))/(Z(L,ISA)-Z(L1,ISA))
      SWH75=SWH75+RTO*SWST(L,ISA)
      SNO75=SNO75+RTO*WNO3(L,ISA)
      SNH75=SNH75+RTO*WNH3(L,ISA)
	  WT75=WT75+RTO*WT(L,ISA)
	  DO J=1,NBSL(ISA)
	      L=LID(J,ISA)
	      IF(Z(L,ISA)>.85)GO TO 10
	      SWH85=SWH85+SWST(L,ISA)
	      SNO85=SNO85+WNO3(L,ISA)
	      SNH85=SNH85+WNH3(L,ISA)
	      WT85=WT85+WT(L,ISA)
	      L1=L
	  END DO
      GO TO 19
10     RTO=(.85-Z(L1,ISA))/(Z(L,ISA)-Z(L1,ISA))
      SWH85=SWH85+RTO*SWST(L,ISA)
      SNO85=SNO85+RTO*WNO3(L,ISA)
      SNH85=SNH85+RTO*WNH3(L,ISA)
	  WT85=WT85+RTO*WT(L,ISA)
!
19    SWH85=(SWH85-SWH75)/100.
      SNO85=1000.*(SNO85-SNO75)/(WT85-WT75)
	  SNH85=1000.*(SNH85-SNH75)/(WT85-WT75)
18    SWH75=(SWH75-SWH65)/100.
      SNO75=1000.*(SNO75-SNO65)/(WT75-WT65)
	  SNH75=1000.*(SNH75-SNH65)/(WT75-WT65)
17    SWH65=(SWH65-SWH55)/100.
      SNO65=1000.*(SNO65-SNO55)/(WT65-WT55)
	  SNH65=1000.*(SNH65-SNH55)/(WT65-WT55)
16    SWH55=(SWH55-SWH45)/100.
      SNO55=1000.*(SNO55-SNO45)/(WT55-WT45)
	  SNH55=1000.*(SNH55-SNH45)/(WT55-WT45)
15    SWH45=(SWH45-SWH35)/100.
      SNO45=1000.*(SNO45-SNO35)/(WT45-WT35)
	  SNH45=1000.*(SNH45-SNH35)/(WT45-WT35)
14    SWH35=(SWH35-SWH25)/100.
      SNO35=1000.*(SNO35-SNO25)/(WT35-WT25)
	  SNH35=1000.*(SNH35-SNH25)/(WT35-WT25)
13    SWH25=(SWH25-SWH15)/100.
      SNO25=1000.*(SNO25-SNO15)/(WT25-WT15)
	  SNH25=1000.*(SNH25-SNH15)/(WT25-WT15)
12    SWH15=(SWH15-SWH05)/100.
      SNO15=1000.*(SNO15-SNO05)/(WT15-WT05)
	  SNH15=1000.*(SNH15-SNH05)/(WT15-WT05)
11    SWH05=SWH05/50.
      SNO05=1000.*SNO05/WT05
	  SNH05=1000.*SNH05/WT05
      RETURN
	  END 