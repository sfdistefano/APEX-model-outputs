      SUBROUTINE NCBL(BC,YCI,YCO,QCI,QCO,YCWN,VBC,RSPC,RSDC,TFOC,BURN,SCOU,&
      FTC,ISA,KW,NBSA,MSO,JRT)
!     APEX1905
!     THIS SUBPROGRAM COMPUTES THE C BALANCE AT THE END OF THE
!     SIMULATION
      DIMENSION NBSA(ISA),KW(MSO)
	  JRT=0
      DF=BC+QCI-QCO+YCI-YCO-YCWN-VBC-RSPC+TFOC+RSDC-BURN-SCOU-FTC
      PER=100.*DF/FTC
      WRITE(KW(1),2)ISA,NBSA(ISA)
	  WRITE(KW(1),1)PER,DF,BC,QCI,QCO,YCI,YCO,YCWN,VBC,RSPC,RSDC,TFOC,&
      BURN,SCOU,FTC
      BC=FTC
      IF(ABS(PER)>1.)WRITE(KW(36),3)ISA,NBSA(ISA),PER
	  JRT=1
      RETURN
    1 FORMAT(5X,'PER =',E13.6,2X,'DF  =',E13.6,2X,'BTOT=',E13.6,2X,&
      'QI  =',E13.6,2X,'QO  =',E13.6,2X,'YI  =',E13.6/5X,'YO  =',E13.6,&
      2X,'YWN =',E13.6,2X,'PRK =',E13.6,2X,'RSPC=',E13.6,2X,'RSDC=',&
      E13.6,2X,'TFOC=',E13.6/5X,'BURN=',E13.6,2X,'SCOU=',E13.6,2X,&
      'FTOT=',E13.6) 
    2 FORMAT(/T10,'C BALANCE(t)',2X,'SA#= ',I8,1X,'ID= ',I8)
    3 FORMAT(/T10,'C BALANCE(t)',2X,'SA#= ',I8,1X,'ID= ',I8,1X,'PER =',&
      E13.6)
      END