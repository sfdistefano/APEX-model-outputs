      SUBROUTINE NMULCH(KTX)
!     APEX1905
!     THIS SUBPROGRAM APPLIES MULCH TO THE SOIL SURFACE AT SPECIFIED DATES AND
!     RATES.
      USE PARM
      I=LID(1,ISA)
      WSAX=WSA(ISA)
      X1=WMUCH(IRO(ISA),KTX,ISA)
      X8=.42*X1
      X3=X8*WSAX
      SMM(73,MO,ISA)=SMM(73,MO,ISA)+X3
      VAR(73,ISA)=VAR(73,ISA)+X3
      X2=X8/50.
      ZZ=.5*X2
      WLMN(I,ISA)=WLMN(I,ISA)+ZZ
      WLSN(I,ISA)=WLSN(I,ISA)+ZZ
      X2=X2*WSAX
      SMM(53,MO,ISA)=SMM(53,MO,ISA)+X2
      VAR(53,ISA)=VAR(53,ISA)+X2
      XX=.5*X8
	  WLMC(I,ISA)=WLMC(I,ISA)+XX
	  YY=.5*X1
	  WLM(I,ISA)=WLM(I,ISA)+YY
	  WLSC(I,ISA)=WLSC(I,ISA)+XX
	  WLSLC(I,ISA)=WLSLC(I,ISA)+XX*.175
	  WLSLNC(I,ISA)=WLSC(I,ISA)-WLSLC(I,ISA)
	  WLS(I,ISA)=WLS(I,ISA)+YY
	  WLSL(I,ISA)=WLSL(I,ISA)+YY*.175
      WRITE(KW(1),11)ISA,NBSA(ISA),IYR,MO,KDA,X1,XHSM(ISA)
      RETURN
   11 FORMAT(1X,2I8,1X,I4,2I2,2X,'MULCH ADDED RATE=',F8.0,'kg/ha',&
      1X,'HUSC=',F5.2)
      END