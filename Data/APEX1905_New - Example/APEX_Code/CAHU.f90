      FUNCTION CAHU(J,K,BASE,NHS)
!     APEX1905
!     THIS SUBPROGRAM ACCUMULATES HEAT UNITS FOR USE IN CPTHU.
      USE PARM
      CAHU=0.
      MO=1
      DO JDA=J,K
          CALL AXMON(JDA,MO)
          IF(JDHU<=366)THEN
              CALL WHLRMX(JDA)
              IF(HRLT<WDRM(ISA).AND.NHS==0)CYCLE
          END IF
          TA=ARALT(TAV,XX)
          TGX0=TA-BASE
          IF(TGX0>0.)CAHU=CAHU+TGX0
      END DO
      RETURN
      END