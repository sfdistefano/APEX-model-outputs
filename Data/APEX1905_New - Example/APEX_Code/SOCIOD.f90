      SUBROUTINE SOCIOD(KK)
!     APEX1905
!     THIS SUBPROGRAM OUTPUTS THE SOIL ORGANIC C AND N VARIABLES DAILY
      USE PARM 
      WRITE(KW(22),30)IYR,MO,KK,ISA,NBSA(ISA)
      WRITE(KW(22),2)(SID(LORG(LID(J,ISA),ISA)),J=1,NBSL(ISA)),SID(11)
      WRITE(KW(22),3)(Z(LID(I,ISA),ISA),I=1,NBSL(ISA))
      WRITE(KW(22),21)(SOIL(12,LID(I,ISA),ISA),I=1,NBSL(ISA)),UW(2,ISA)
      WRITE(KW(22),27)(STMP(LID(I,ISA),ISA),I=1,NBSL(ISA))
      WRITE(KW(22),28)(RSD(LID(I,ISA),ISA),I=1,NBSL(ISA)),TRSD(ISA)
      X1=.001*ZOC(ISA)
      WRITE(KW(22),14)(WOC(LID(I,ISA),ISA),I=1,NBSL(ISA)),X1
      WRITE(KW(22),8)WBMX
      WRITE(KW(22),1)(RSPC(LID(I,ISA),ISA),I=1,NBSL(ISA)),TRSP
      WRITE(KW(22),4)(RNMN(LID(I,ISA),ISA),I=1,NBSL(ISA)),SNMN
      WRITE(KW(22),5)(FOP(LID(I,ISA),ISA),I=1,NBSL(ISA)),ZFOP(ISA)
      RETURN
