      SUBROUTINE DUSTEM
!     APEX1905
!     THIS SUBPROGRAM ESTIMATES THE PARTICULATE MATTER(DUST) EMISSION
!     RATE FROM FEEDYARDS.
      USE PARM
      LD1=LID(1,ISA)
      DEMR=32.*NHD(IDFH(ISA),IDON(ISA))
      IF(STLT(ISA)>FC(LD1,ISA)-S15(LD1,ISA))THEN
          RTO=800.*SWLT(ISA)/RSD(LD1,ISA)
      ELSE
          RTO=10.*SWST(LD1,ISA)
      END IF
      !IF(SCLM(19)>0.)RTO=MIN(RTO,SCLM(19))
      F=1.-RTO/(RTO+EXP(11.31-.442*RTO))
      DEMR=.001*DEMR*F
      EM10(ISA)=EM10(ISA)+DEMR
      SMM(93,MO,ISA)=SMM(93,MO,ISA)+DEMR
      RETURN
      END      